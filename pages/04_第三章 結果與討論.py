st.header("3.1人臉辨識測試結果與說明")
st.write("課程首先針對人臉辨識模組進行測試，以攝影機即時影像作為輸入，透過深度學習模型進行人臉偵測與辨識。實驗結果顯示，系統能夠在一般室內光源環境下即時擷取人臉位置，並正確辨識目標人臉。在多張人臉同時出現的情境中，模型仍可分別標示各人臉之邊界框，顯示其具備良好的多目標辨識能力。然而，當光照不足或人臉角度偏移過大時，辨識準確率略有下降，顯示模型對於極端光影與姿態變化仍存在一定限制。整體而言，人臉辨識模組具備即時性與基本穩定度，可應用於門禁辨識、使用者身份確認等相關應用情境。")

st.header("3.2物件尺寸量測測試結果與說明")
st.write("在物件尺寸量測實驗中，本研究利用影像中物件之邊界資訊，結合相機標定結果，將像素距離轉換為實際物理尺寸。透過已知尺寸之測試物件進行驗證，系統所量測之長度與實際尺寸之誤差控制於可接受範圍內。實驗結果顯示，當物件平面與相機視角近似垂直時，量測結果最為準確；反之，若物件產生傾斜或距離鏡頭過近，則可能因透視誤差導致量測誤差增加。此結果顯示相機姿態與物件擺放方式對量測準確度具有顯著影響。綜合測試結果可知，本系統在一般工件或物品尺寸估測上具備實用價值，適合應用於初步尺寸量測與自動化檢測輔助。")

st.header("3.3 OCR辨識測試結果與說明")
st.write("文字光學辨識（OCR）模組主要針對影像中的數字與英文字元進行辨識測試。實驗中採用不同字型、字體大小及背景條件之影像作為測試資料，以評估系統之辨識能力。測試結果顯示，在文字清晰、背景對比明顯的情況下，OCR 模組可正確辨識大部分字元，整體辨識率表現良好。然而，當文字出現模糊、反光或背景雜訊較多時，部分字元容易產生誤判或漏判情形。此結果顯示，影像前處理（如對比增強、雜訊抑制）對 OCR 辨識結果具有重要影響。整體而言，OCR 模組可有效應用於標籤讀取、序號辨識及文件數位化等應用。")

st.header("3.4 yolo物件辨識測試結果與說明")
st.write("課程採用 YOLO（You Only Look Once）深度學習模型進行物件辨識實驗，透過自行訓練之資料集，使模型能辨識特定物件類別。測試結果顯示，YOLO 模型可即時完成物件定位與分類，並以邊界框方式標示辨識結果。在即時攝影機畫面中，系統能同時辨識多個物件，並即時計算各類物件數量，顯示 YOLO 模型在速度與準確度之間具有良好平衡。然而，當物件重疊或尺寸過小時，辨識結果可能出現漏判情形，顯示資料集完整性與訓練品質仍為影響辨識效能之關鍵因素。整體而言，YOLO 物件辨識模組展現良好之即時性與擴充性，適合應用於智慧監控、自動分類與即時盤點等應用場景。")

